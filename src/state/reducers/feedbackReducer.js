const feedbackReducer = (state = origin, action) => {
  const { type, targetId } = action;

  switch (type) {
    case ACTION.SWITCH_USE_CASE_CHART:
      if (targetId === "leadValue") {
        return {
          ...state,
          useCases: {
            activeChart: "leadValue",
            mainSeries: valueMainSeries,
            tooltipSeries: valueTooltipSeries,
            collapseSeries: valueCollapseSeries
          }
        };
      } else {
        return {
          ...state,
          useCases: {
            ...state.useCases,
            activeChart: "execConversion",
            mainSeries: execMainSeries,
            tooltipSeries: execTooltipSeries,
            collapseSeries: execCollapseSeries
          }
        }
      }

    default:
      return state;
  }
}

